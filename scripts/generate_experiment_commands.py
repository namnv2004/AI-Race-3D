#!/usr/bin/env python3
"""Generate shell command files for the best-single-method experiment plan.

This script only writes command files. It does not execute training, rendering,
evaluation, or any GPU workload.
"""

from __future__ import annotations

import argparse
import json
import shlex
import sys
from pathlib import Path
from typing import Any

PYTHON = sys.executable

METHOD_SCRIPTS = {
    "3dgs": "scripts/train_gs_scene.py",
    "mip": "scripts/run_mip_splatting_scene.py",
    "2dgs": "scripts/run_2dgs_scene.py",
}


def shell_join(parts: list[str | Path | int | float]) -> str:
    return " ".join(shlex.quote(str(part)) for part in parts)


def flag_name(key: str) -> str:
    return "--" + key.replace("_", "-")


def append_args(command: list[str], args: dict[str, Any]) -> list[str]:
    for key, value in args.items():
        if value is None or value is False:
            continue
        flag = flag_name(key)
        if value is True:
            command.append(flag)
        elif isinstance(value, list):
            command.append(flag)
            command.extend(str(item) for item in value)
        else:
            command.extend([flag, str(value)])
    return command


def load_plan(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def experiments_for_phase(plan: dict[str, Any], phase: str) -> list[dict[str, Any]]:
    return [experiment for experiment in plan["experiments"] if phase in experiment.get("phases", [])]


def phase_scenes(plan: dict[str, Any], phase: str) -> list[str]:
    if phase == "quick":
        return list(plan["quick_scenes"])
    if phase == "public":
        return list(plan["public_scenes"])
    if phase == "private":
        return list(plan["private_scenes"])
    raise ValueError(f"Unsupported phase: {phase}")


def phase_root(plan: dict[str, Any], phase: str) -> Path:
    if phase in {"quick", "public"}:
        return Path(plan["public_root"])
    if phase == "private":
        return Path(plan["private_root"])
    raise ValueError(f"Unsupported phase: {phase}")


def output_dir(plan: dict[str, Any], phase: str, experiment_id: str) -> Path:
    if phase == "private":
        return Path(plan["submission_output_dir"])
    return Path(plan["outputs_root"]) / phase / experiment_id


def common_model_args(plan: dict[str, Any], phase: str, experiment: dict[str, Any]) -> dict[str, str]:
    experiment_id = str(experiment["id"])
    method = str(experiment["method"])
    safe_phase = "private" if phase == "private" else phase
    args: dict[str, str] = {}
    if method == "mip":
        args["model_root"] = str(Path(plan["checkpoints_root"]) / safe_phase / experiment_id)
        args["workspace_dir"] = str(Path(plan["workspace_root"]) / "mip_splatting" / safe_phase / experiment_id)
    elif method == "2dgs":
        args["model_root"] = str(Path(plan["checkpoints_root"]) / safe_phase / experiment_id)
    return args


def train_command(plan: dict[str, Any], phase: str, experiment: dict[str, Any], scene: str) -> str:
    method = str(experiment["method"])
    script = METHOD_SCRIPTS[method]
    args = dict(experiment.get("args", {}))
    args = common_model_args(plan, phase, experiment) | args
    command = [PYTHON, script, "--scene-dir", str(phase_root(plan, phase) / scene), "--output-dir", str(output_dir(plan, phase, str(experiment["id"])))]
    append_args(command, args)
    return shell_join(command)


def eval_command(plan: dict[str, Any], phase: str, experiment: dict[str, Any]) -> str:
    eval_config = plan.get("eval", {})
    eval_path = Path(plan["reports_root"]) / "eval" / phase / f"{experiment['id']}.json"
    command = [
        PYTHON,
        "scripts/evaluate_public.py",
        "--public-root",
        plan["public_root"],
        "--pred-dir",
        str(output_dir(plan, phase, str(experiment["id"]))),
        "--scenes",
        *phase_scenes(plan, phase),
        "--json-out",
        str(eval_path),
    ]
    if eval_config.get("with_lpips", True):
        command.append("--with-lpips")
    if eval_config.get("lpips_net"):
        command.extend(["--lpips-net", str(eval_config["lpips_net"])])
    if eval_config.get("lpips_device"):
        command.extend(["--lpips-device", str(eval_config["lpips_device"])])
    return shell_join(command)


def write_script(path: Path, title: str, commands: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "#!/usr/bin/env bash",
        "set -euo pipefail",
        "",
        f"# {title}",
        "# Generated by scripts/generate_experiment_commands.py",
        "# Review before running. This file may start GPU workloads.",
        "",
    ]
    lines.extend(commands)
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")
    path.chmod(0o755)


def mkdir_command(path: Path) -> str:
    return shell_join(["mkdir", "-p", str(path)])


def generate_preflight(plan: dict[str, Any], out_dir: Path) -> Path:
    path = out_dir / "00_preflight.sh"
    commands = [
        mkdir_command(Path(plan["reports_root"])),
        shell_join([PYTHON, "scripts/inspect_dataset.py", "--json-out", str(Path(plan["reports_root"]) / "inspect_latest.json")]),
        shell_join([PYTHON, "scripts/train_gs_scene.py", "--help"]),
        shell_join([PYTHON, "scripts/run_mip_splatting_scene.py", "--help"]),
        shell_join([PYTHON, "scripts/run_2dgs_scene.py", "--help"]),
        shell_join([PYTHON, "scripts/evaluate_public.py", "--help"]),
    ]
    write_script(path, "Non-GPU preflight checks", commands)
    return path


def generate_masks(plan: dict[str, Any], out_dir: Path) -> Path:
    path = out_dir / "01_generate_masks.sh"
    commands = [
        shell_join([PYTHON, "scripts/generate_sam3_masks.py", "--split-root", plan["public_root"], "--output-dir", plan["mask_root"]]),
        shell_join([PYTHON, "scripts/generate_sam3_masks.py", "--split-root", plan["private_root"], "--output-dir", plan["mask_root"]]),
    ]
    write_script(path, "Optional SAM mask generation for mask-weighted experiments", commands)
    return path


def generate_train_phase(plan: dict[str, Any], out_dir: Path, phase: str) -> Path:
    path = out_dir / ("10_quick_train.sh" if phase == "quick" else "20_public_train.sh")
    commands: list[str] = []
    for experiment in experiments_for_phase(plan, phase):
        commands.append(f"# experiment: {experiment['id']} ({experiment['method']})")
        for scene in phase_scenes(plan, phase):
            commands.append(train_command(plan, phase, experiment, scene))
        commands.append("")
    write_script(path, f"Train/render {phase} experiments", commands)
    return path


def generate_eval_phase(plan: dict[str, Any], out_dir: Path, phase: str) -> Path:
    path = out_dir / ("11_quick_eval.sh" if phase == "quick" else "21_public_eval.sh")
    commands = [mkdir_command(Path(plan["reports_root"]) / "eval" / phase)]
    commands.extend(eval_command(plan, phase, experiment) for experiment in experiments_for_phase(plan, phase))
    write_script(path, f"Evaluate {phase} experiments", commands)
    return path


def generate_private(plan: dict[str, Any], out_dir: Path, selected_experiment_id: str) -> Path:
    experiments = {str(experiment["id"]): experiment for experiment in plan["experiments"]}
    if selected_experiment_id not in experiments:
        raise SystemExit(f"Unknown selected experiment: {selected_experiment_id}")
    experiment = experiments[selected_experiment_id]
    path = out_dir / "30_private_best_single_method.sh"
    commands = [
        f"# selected experiment: {experiment['id']} ({experiment['method']})",
        mkdir_command(Path(plan["submission_output_dir"])),
    ]
    for scene in phase_scenes(plan, "private"):
        commands.append(train_command(plan, "private", experiment, scene))
    commands.extend(
        [
            "",
            shell_join([PYTHON, "scripts/verify_submission.py", "--split-root", plan["private_root"], "--submission-dir", plan["submission_output_dir"]]),
            shell_join([PYTHON, "scripts/make_submission_zip.py", "--submission-dir", plan["submission_output_dir"], "--zip", plan["submission_zip"], "--overwrite"]),
            shell_join([PYTHON, "scripts/verify_zip.py", "--split-root", plan["private_root"], "--zip", plan["submission_zip"]]),
        ]
    )
    write_script(path, "Render private set with exactly one selected method/config", commands)
    return path


def generate_selection_helper(plan: dict[str, Any], out_dir: Path, config_path: Path) -> Path:
    path = out_dir / "22_select_best_single_method.sh"
    command = shell_join(
        [
            PYTHON,
            "scripts/select_best_single_method.py",
            "--config",
            str(config_path),
            "--eval-dir",
            str(Path(plan["reports_root"]) / "eval" / "public"),
            "--json-out",
            str(Path(plan["reports_root"]) / "final_selection.json"),
        ]
    )
    write_script(path, "Select one global best method/config from public eval JSON files", [command])
    return path


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate experiment command files without executing them.")
    parser.add_argument("--config", type=Path, default=Path("configs/best_single_method_plan.json"))
    parser.add_argument("--out-dir", type=Path, default=Path("reports/experiments/commands"))
    parser.add_argument("--selected-experiment", default=None, help="Generate private render script for this selected experiment id.")
    args = parser.parse_args()

    plan = load_plan(args.config)
    generated = [
        generate_preflight(plan, args.out_dir),
        generate_masks(plan, args.out_dir),
        generate_train_phase(plan, args.out_dir, "quick"),
        generate_eval_phase(plan, args.out_dir, "quick"),
        generate_train_phase(plan, args.out_dir, "public"),
        generate_eval_phase(plan, args.out_dir, "public"),
        generate_selection_helper(plan, args.out_dir, args.config),
    ]
    if args.selected_experiment:
        generated.append(generate_private(plan, args.out_dir, args.selected_experiment))

    print("Generated command files:")
    for path in generated:
        print(path)


if __name__ == "__main__":
    main()
